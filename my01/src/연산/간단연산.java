package 연산;

public class 간단연산 {

	public static void main(String[] args) {
		
		System.out.println(100 + 200);
		
		int n1 = 200;
		int n2 = 100;
		int n3 = n1 + n2;
		
		System.out.println("두 수의 합은 " + n3); // RAM에 저장, CPU가 계산
		
		int n4 = n3 + 100;
		
		System.out.println("세 수의 합은 " + n4);
		
		// 1. 변수 - 데이터
		// 2. 연산 - 계산
		// 3. 제어 - 반복, 조건, 순서
		// -----------------------
		// 4. 부품조립방법
		
	}

}
