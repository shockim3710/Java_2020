package 제어;

public class 네이버로그인 {

	public static void main(String[] args) {

		// 비교 연산자는 조건에 사용되고
		// 비교 연산자의 결과는 무조건 boolean
		
		int id = 1111;
		
		if(id == 2222) { //조건이 맞으면 괄호에 있는게 실행
		
			System.out.println("로그인이 되었습니다.");

		}
		
		else { //조건이 안맞으면 괄호에 있는게 실행
			
			System.out.println("로그인에 실패하였습니다.");

		}	
		
	}
	
}